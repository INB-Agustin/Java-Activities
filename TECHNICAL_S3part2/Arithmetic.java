package TECHNICAL_S3part2;
public class Arithmetic {
    public void Add(int num1, int num2){
    System.out.println("Sum = " + (num1 + num2));
    }
    public void Add(int num1, int num2, int num3){
    System.out.println("Sum = " + (num1 + num2 + num3));
    }
    public void Add(int num1, int num2, int num3, int num4){
    System.out.println("Sum = " + (num1 + num2 + num3 + num4));
    }
    public void Multiply(int num1, int num2){
    System.out.println("Product = " + (num1 * num2));
    }
    public void Multiply(int num1, int num2, int num3){
    System.out.println("Product = " + (num1 * num2 * num3));
    }
    public void Multiply(int num1, int num2, int num3, int num4){
    System.out.println("Sum = " + (num1 * num2 * num3 * num4));
    }
    public void Subtract(int num1, int num2){
    System.out.println("Difference = " + (num1 - num2));
    }
    public void Divide(int num1, int num2){
    System.out.println("Quotient = " + (num1 / num2));
    }
}
